      <h1 style='text-indent:0cm'><a name="_Toc41941587">INTRODUCCIÓN</a></h1>

      <p class=MsoNormal style='text-align:justify'><span style='font-size:12.0pt;
line-height:115%;color:#4A86E8'>&nbsp;</span></p>

      <p class=MsoNoSpacing style='margin-top:12.0pt;text-align:justify'><span style='line-height:150%;font-family:"Arial",sans-serif'>El sistema de
            información fue diseñado para facilitar la venta de productos y la gestión de
            inventarios de la empresa Marisol Vivero S.A.S., ubicada en el corregimiento
            Santa Elena (Medellín, Antioquia).</span></p>

      <p class=MsoNoSpacing style='margin-top:12.0pt;text-align:justify'><span style='line-height:150%;font-family:"Arial",sans-serif'>En su página de inicio
            se encuentra la interfaz que muestra los datos de contracto de la empresa, el
            menú de navegación, una sección con información interés y enlaces para acceder
            al catálogo de productos mediante un filtro por categorías.</span></p>

      <p class=MsoNoSpacing style='margin-top:12.0pt;text-align:justify'><span style='line-height:150%;font-family:"Arial",sans-serif'>El sistema también
            cuenta con un área para clientes a través de la cual pueden realizar un
            registro previo en el sistema y comprar productos. Asimismo, existe un área de
            administración para el uso de usuarios internos previamente autorizados, que
            podrán realizar tareas administrativas como gestión de clientes, gestión de
            inventarios, generación de informes y creación de pedidos de ventas
            presenciales (esto último aún no se ha implementado).</span></p>

      <p class=MsoNoSpacing style='margin-top:12.0pt;text-align:justify'><span style='line-height:150%;font-family:"Arial",sans-serif'>El presente manual está
            enfocado en el uso del sistema a través de un servidor local, mediante la
            herramienta XAMPP (</span><a href="http://www.apachefriends.org"><span style='line-height:150%;font-family:"Arial",sans-serif'>www.apachefriends.org</span></a><span style='line-height:150%;font-family:"Arial",sans-serif'>).</span><span style='font-family:"Arial",sans-serif'><br clear=all style='page-break-before:
