      <h1 style='text-align:justify'><a name="_Toc41941590">2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span>REQUISITOS DE CONOCIMIENTO PARA OPERAR EL SISTEMA</a></h1>

      <p class=MsoNormal style='margin-top:12.0pt;text-align:justify;line-height:
normal'>Para la operación del sistema sólo se requiere tener conocimientos
